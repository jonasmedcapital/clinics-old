class Operations::Accounts::Products::List

  def initialize(params)
    @product = product
  end

  def product
    ::Operations::Accounts::ProductRepository.all_active
  end

  def status
    @status
  end

  def process?
    @process
  end

  def type
    @type
  end

  def message
    @message
  end

  def data
    cln = ::Operations::Accounts::ProductRepository.list_all(@product)
    
    if cln.empty?
      @status = :ok
      @process = false
      @message = "NÃ£o conseguimos carregar os dados"
      @type = "danger"
    else
      @status = :ok
      @process = true
      @message = ""
      @type = "success"
    end
    
    return {:cln => cln.compact}.as_json
  end

end
class Operations::Products::Entities::List

  def initialize(params)
    @products = products
  end

  def products
    ::Operations::Products::EntityRepository.all_active
  end

  def status
    @status
  end

  def process?
    @process
  end

  def type
    @type
  end

  def message
    @message
  end

  def data
    cln = ::Operations::Products::EntityRepository.list_all(@products)
    
    if cln.empty?
      @status = :ok
      @process = false
      @message = "NÃ£o conseguimos carregar os dados"
      @type = "danger"
    else
      @status = :ok
      @process = true
      @message = ""
      @type = "success"
    end
    
    return {:cln => cln.compact}.as_json
  end

end
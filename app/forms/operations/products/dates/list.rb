class Operations::Products::Dates::List

  def initialize(params)
    @date = date
  end

  def date
    ::Operations::Products::DateRepository.all_active
  end

  def status
    @status
  end

  def process?
    @process
  end

  def type
    @type
  end

  def message
    @message
  end

  def data
    cln = ::Operations::Products::DateRepository.list_all(@date)
    
    if cln.empty?
      @status = :ok
      @process = false
      @message = "NÃ£o conseguimos carregar os dados"
      @type = "danger"
    else
      @status = :ok
      @process = true
      @message = ""
      @type = "success"
    end
    
    return {:cln => cln.compact}.as_json
  end

end